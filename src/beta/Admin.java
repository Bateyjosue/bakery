    private void btn_editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_editMouseClicked
        try
        {
            if(!kon.isClosed())
            {
                String s="UPDATE `users` SET `id`='"+txt_usr_ID.getText()+"',`usernames`='"+txt_usr_NAME.getText()+"',"
                + "`passwords`='"+txt_usr_PASSWORD.getText()+"',`types`='"+txt_usr_TYPE.getText()+"', "
                        + "phones = '"+txt_usr_contact.getText()+"'"
                        + " WHERE `id`='"+txt_usr_ID.getText()+"'";
                pst=kon.prepareStatement(s);
                pst.execute();
                JOptionPane.showMessageDialog(null,"Account Confirmed successfully");
                table();
            }
            else
                kon.close();
        }
        catch(SQLException | HeadlessException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btn_editMouseClicked

